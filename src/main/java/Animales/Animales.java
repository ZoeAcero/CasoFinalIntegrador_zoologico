package Animales;

public class Animales {

    String nombre;
    String tipo;
    String raza;
    String color;
    String edad;
    Float peso;
    Float altura;

    public Animales(String nombre, String tipo, String raza, String color, String edad, Float peso, Float altura) {
        this.nombre = nombre;
        this.tipo = tipo;
        this.raza = raza;
        this.color = color;
        this.edad = edad;
        this.peso = peso;
        this.altura = altura;
    }
    


}


